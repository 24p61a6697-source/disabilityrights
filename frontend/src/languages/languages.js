/* ---------------- CONSTANTS ---------------- */

const DEFAULT_LANG = "en";
const VALID_LANGS = new Set(INDIAN_LANGUAGES.map(l => l.code));

/* ---------------- AUTOCORRECTION ---------------- */

const LANGUAGE_AUTOCORRECTIONS = {
  ml: [
    [/ഗവണ്മെന്റ്/gi, "സർക്കാർ"],
    [/ചാറ്റ് ഹിസ്റ്ററി/gi, "ചാറ്റ് ചരിത്രം"],
  ],
  te: [
    [/కొనసాగించు/gi, "కొనసాగించు"],
  ],
};

function applyAutoCorrections(text, lang) {
  if (typeof text !== "string") return text;

  const rules = LANGUAGE_AUTOCORRECTIONS[lang];
  if (!rules) return text;

  let result = text;
  for (const [pattern, replacement] of rules) {
    result = result.replace(pattern, replacement);
  }
  return result;
}

/* ---------------- MAIN TRANSLATOR ---------------- */

export function t(key, language) {
  const lang = VALID_LANGS.has(language) ? language : DEFAULT_LANG;

  const langPack = UI_TRANSLATIONS[lang] || {};
  const fallbackPack = UI_TRANSLATIONS[DEFAULT_LANG];

  let text = langPack[key];

  if (!text) {
    console.warn(`Missing translation key "${key}" for language "${lang}"`);
    text = fallbackPack[key] || key;
  }

  return applyAutoCorrections(text, lang);
}